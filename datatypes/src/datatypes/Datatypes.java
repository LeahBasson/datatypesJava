
package datatypes;


public class Datatypes {

    
    public static void main(String[] args) {
        int x=10;
        int y=20;
        
        String greeting="hello";
        String person="my friend";
        
        double decimal1=10;
        double decimal2=20;
        
        char symbol='#';
        char initial='L';
        
        boolean yes=true;
        boolean no=false;
        
        System.out.println(x + " "+y);
        System.out.println(greeting+ " "+person);
        System.out.println(decimal1 + " "+decimal2);
        System.out.println(symbol + " "+initial);
        System.out.println(yes + " "+no);
    }
    
}
